#operasi logika atau boolean
# not, or, and, xor

#not
print('===NOT===')
a = True
c = not a
print('data a, = ', a)
print('----------NOT')
print('data c, = ', c)

#or
print('===OR===')
a = False
b = True
c = a or b
print (a, 'or', b, '=', c)

#and
print('===AND===')
a = True
b = False
c = a and b
print (a, 'and', b, '=', c)
